def main():
    args = parse_args()
    root = Path(__file__).resolve().parent.parent
    report_path = Path(args.report)
    feedback_path = Path(args.feedback)
    skill_pack_path = Path(args.skill_pack)
    skill_output_path = Path(args.skill_output)

    if not report_path.exists():
        print(f"[optimizer] ERROR: report not found: {report_path}")
        sys.exit(1)
    if not feedback_path.exists():
        print(f"[optimizer] ERROR: feedback not found: {feedback_path}")
        sys.exit(1)
    if not skill_pack_path.exists():
        print(f"[optimizer] ERROR: skill pack not found: {skill_pack_path}")
        sys.exit(1)

    report = json.loads(report_path.read_text(encoding="utf-8"))
    feedback = json.loads(feedback_path.read_text(encoding="utf-8"))
    pack = json.loads(skill_pack_path.read_text(encoding="utf-8"))
    before_total = get_total(report)

    proposal, next_pack = build_proposal(
        report,
        feedback,
        pack,
        args.round,
        max_step_changes=args.max_step_changes,
        use_llm=args.use_llm,
        model=args.model,
        principles_dir=args.principles_dir,
        cwd=root,
    )
    backup = skill_pack_path.with_suffix(".json.bak")
    backup.write_text(json.dumps(pack, indent=2, ensure_ascii=False), encoding="utf-8")
    skill_pack_path.write_text(json.dumps(next_pack, indent=2, ensure_ascii=False), encoding="utf-8")
    render_skill(root, skill_pack_path, skill_output_path)

    gate = run_gate(args.gate_cmd)
    decision = "accepted"
    after_total = before_total
    reject_reasons = []
    gate_compare = {}
    rolled_back = False

    if not args.gate_cmd:
        decision = reject_with_rollback(
            root,
            pack,
            skill_pack_path,
            skill_output_path,
            "rejected_gate_not_configured",
            "gate_cmd_not_provided",
            reject_reasons,
        )
        rolled_back = True
    elif not args.gate_report:
        decision = reject_with_rollback(
            root,
            pack,
            skill_pack_path,
            skill_output_path,
            "rejected_gate_report_missing",
            "gate_report_path_not_provided",
            reject_reasons,
        )
        rolled_back = True
    else:
        if not gate["executed"]:
            decision = reject_with_rollback(
                root,
                pack,
                skill_pack_path,
                skill_output_path,
                "rejected_gate_not_executed",
                "gate_not_executed",
                reject_reasons,
            )
            rolled_back = True
        elif gate["exit_code"] != 0:
            decision = reject_with_rollback(
                root,
                pack,
                skill_pack_path,
                skill_output_path,
                "rejected_gate_cmd_failed",
                f"gate_cmd_failed: exit_code={gate['exit_code']}",
                reject_reasons,
            )
            rolled_back = True
        else:
            gate_report_path = Path(args.gate_report)
            if not gate_report_path.exists():
                decision = reject_with_rollback(
                    root,
                    pack,
                    skill_pack_path,
                    skill_output_path,
                    "rejected_gate_report_missing",
                    f"gate_report_not_found: {gate_report_path}",
                    reject_reasons,
                )
                rolled_back = True
            else:
                after_report = json.loads(gate_report_path.read_text(encoding="utf-8"))
                gate_compare = evaluate_gate(
                    report,
                    after_report,
                    branch_tolerance=args.branch_drop_tolerance,
                    pass_rate_drop_tol=args.pass_rate_drop_tolerance,
                )
                after_total = gate_compare["after_total"]
                if not gate_compare["accept"]:
                    decision = "rejected_gate_metrics"
                    reject_reasons.extend(gate_compare["reject_reasons"])
                    rollback_to_pack(root, pack, skill_pack_path, skill_output_path)
                    rolled_back = True

    # Distill principle after gate decision
    if args.principles_dir and args.project_name:
        distill_principle(
            proposal=proposal,
            decision=decision,
            before_total=before_total,
            after_total=after_total,
            feedback=feedback,
            round_num=args.round,
            principles_dir=args.principles_dir,
            project_name=args.project_name,
        )

    result = {
        "round": args.round,
        "timestamp": datetime.now(timezone.utc).isoformat(),
        "proposal": proposal,
        "before_total": before_total,
        "after_total": after_total,
        "gate": gate,
        "decision": decision,
        "accepted": decision == "accepted",
        "rolled_back": rolled_back,
        "reject_reasons": reject_reasons,
        "gate_compare": gate_compare,
        "backup": str(backup),
    }

    if args.result:
        result_path = Path(args.result)
    else:
        result_path = root / "results" / f"optimizer_round_{args.round}.json"
    result_path.parent.mkdir(parents=True, exist_ok=True)
    result_path.write_text(json.dumps(result, indent=2, ensure_ascii=False), encoding="utf-8")

    print(f"[optimizer] round={args.round} decision={decision} method={proposal.get('method', 'rules')}")
    print(f"[optimizer] result={result_path}")


if __name__ == "__main__":
    main()
