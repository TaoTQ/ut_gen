def build_proposal(
    report: dict,
    feedback: dict,
    pack: dict,
    round_num: int,
    max_step_changes: int = 2,
    use_llm: bool = True,
    model: str = "deepseek/deepseek-chat",
    principles_dir: str = "",
    cwd: Path | None = None,
) -> tuple[dict, dict]:
    if use_llm:
        if cwd is None:
            cwd = Path(__file__).resolve().parent.parent
        result = build_proposal_llm(
            report, feedback, pack, round_num,
            model=model,
            principles_dir=principles_dir,
            cwd=cwd,
            max_step_changes=max_step_changes,
        )
        if result is not None:
            print("[optimizer] LLM proposal generated successfully")
            return result
        print("[optimizer] LLM failed, falling back to rules")
    return build_proposal_rules(report, feedback, pack, round_num, max_step_changes)


