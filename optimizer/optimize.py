def build_proposal_rules(report: dict, feedback: dict, pack: dict, round_num: int, max_step_changes: int = 2) -> dict:
    total = get_total(report)
    method_gaps = feedback.get("method_gaps", [])
    weak_methods = method_gaps[:8]
    step_limit = max(1, min(2, int(max_step_changes)))

    proposal = {
        "round": round_num,
        "timestamp": datetime.now(timezone.utc).isoformat(),
        "before_total": total,
        "changes": [],
        "max_step_changes": step_limit,
        "parameter_changes": [],
        "skipped_candidates": [],
        "focus_hints": [f"{x['class']}.{x['method']} branch={x['branch']}%" for x in weak_methods],
        "method": "rules",
    }

    generation = copy.deepcopy(pack.get("generation", {}))
    rules = copy.deepcopy(pack.get("rules", {}))
    targets = copy.deepcopy(pack.get("targets", {}))

    def try_change(param: str, before_value, after_value, message: str, expected_benefit: str) -> bool:
        if len(proposal["parameter_changes"]) >= step_limit:
            proposal["skipped_candidates"].append({"param": param, "reason": "step_limit_reached"})
            return False
        if before_value == after_value:
            proposal["skipped_candidates"].append({"param": param, "reason": "no_effect"})
            return False
        proposal["parameter_changes"].append(
            {
                "param": param,
                "before": before_value,
                "after": after_value,
                "expected_benefit": expected_benefit,
            }
        )
        proposal["changes"].append(message)
        return True

    if total.get("branch", 0.0) < targets.get("branch_pct", 85.0):
        current = int(generation.get("min_cases_per_method", 3))
        updated = min(6, current + 1)
        if try_change(
            "generation.min_cases_per_method",
            current,
            updated,
            f"increase generation.min_cases_per_method: {current} -> {updated}",
            "more candidate test cases per method can improve branch exploration",
        ):
            generation["min_cases_per_method"] = updated

        branch_focus = list(rules.get("branch_focus", []))
        hint = "for every uncovered branch, add at least one branch-forcing test input"
        if hint in branch_focus:
            proposal["skipped_candidates"].append({"param": "rules.branch_focus", "reason": "hint_already_present"})
        else:
            before = len(branch_focus)
            branch_focus.append(hint)
            if try_change(
                "rules.branch_focus",
                before,
                len(branch_focus),
                "append rules.branch_focus hint for branch-forcing inputs",
                "explicit branch-forcing hints can target missed condition paths",
            ):
                rules["branch_focus"] = branch_focus

    if total.get("line", 0.0) < targets.get("line_pct", 90.0):
        boundary = list(rules.get("boundary_three_point", []))
        hint = "for string and collection APIs, include null, empty, singleton, and oversized inputs"
        if hint in boundary:
            proposal["skipped_candidates"].append({"param": "rules.boundary_three_point", "reason": "hint_already_present"})
        else:
            before = len(boundary)
            boundary.append(hint)
            if try_change(
                "rules.boundary_three_point",
                before,
                len(boundary),
                "append rules.boundary_three_point hint for structural edge cases",
                "boundary-focused templates can expose uncovered lines around input checks",
            ):
                rules["boundary_three_point"] = boundary

    if total.get("method", 0.0) < targets.get("method_pct", 85.0):
        quality = list(rules.get("quality", []))
        hint = "ensure every public method appears in at least one explicit test name"
        if hint in quality:
            proposal["skipped_candidates"].append({"param": "rules.quality", "reason": "hint_already_present"})
        else:
            before = len(quality)
            quality.append(hint)
            if try_change(
                "rules.quality",
                before,
                len(quality),
                "append rules.quality hint for method-level coverage",
                "method-name inclusion guidance reduces missed public API methods",
            ):
                rules["quality"] = quality

    if not proposal["parameter_changes"]:
        proposal["changes"].append("no-op: no eligible parameter update under current constraints")

    risk_level, risk_reasons = classify_risk(proposal["parameter_changes"])
    proposal["risk_level"] = risk_level
    proposal["risk_reasons"] = risk_reasons
    proposal["expected_benefit"] = summarize_expected_benefit(proposal["parameter_changes"])

    if risk_level != "low":
        proposal["changes"].append(f"risk_assessment={risk_level}: " + "; ".join(risk_reasons))

    next_pack = copy.deepcopy(pack)
    next_pack["generation"] = generation
    next_pack["rules"] = rules
    next_pack["focus_hints"] = proposal["focus_hints"]
    next_pack["version"] = f"v3.{round_num}"
    return proposal, next_pack


