def distill_principle(
    proposal: dict,
    decision: str,
    before_total: dict,
    after_total: dict,
    feedback: dict,
    round_num: int,
    principles_dir: str,
    project_name: str,
) -> None:
    if not principles_dir or not project_name:
        return

    principles_path = Path(principles_dir) / f"{project_name}.json"

    # Load existing
    principles = []
    if principles_path.exists():
        try:
            data = json.loads(principles_path.read_text(encoding="utf-8"))
            principles = data.get("principles", [])
        except (json.JSONDecodeError, OSError):
            pass

    # Compute coverage delta
    line_delta = float(after_total.get("line", 0)) - float(before_total.get("line", 0))
    branch_delta = float(after_total.get("branch", 0)) - float(before_total.get("branch", 0))
    method_delta = float(after_total.get("method", 0)) - float(before_total.get("method", 0))

    # Determine type
    is_accepted = decision == "accepted"
    principle_type = "guiding" if is_accepted else "cautionary"

    # Change summary
    changes = proposal.get("changes", [])
    change_summary = "; ".join(changes[:3]) if changes else "no changes"

    # Affected methods from feedback
    method_gaps = feedback.get("method_gaps", [])
    affected_methods = [f"{g['class']}.{g['method']}" for g in method_gaps[:5]]

    # Reasoning
    reasoning = proposal.get("reasoning", "") or proposal.get("expected_benefit", "")

    # Confidence
    confidence = min(1.0, abs(branch_delta) / 5.0)

    # Build principle ID
    pid = f"p_{len(principles) + 1:03d}"

    principle = {
        "id": pid,
        "project": project_name,
        "type": principle_type,
        "round": round_num,
        "timestamp": datetime.now(timezone.utc).isoformat(),
        "change_summary": change_summary,
        "coverage_delta": {
            "line": round(line_delta, 2),
            "branch": round(branch_delta, 2),
            "method": round(method_delta, 2),
        },
        "affected_methods": affected_methods,
        "reasoning": reasoning[:300],
        "confidence": round(confidence, 2),
    }

    principles.append(principle)

    # Trim to max 50
    if len(principles) > 50:
        principles = principles[-50:]

    principles_path.parent.mkdir(parents=True, exist_ok=True)
    principles_path.write_text(
        json.dumps({"principles": principles}, indent=2, ensure_ascii=False),
        encoding="utf-8",
    )
    print(f"[optimizer] principle {pid} ({principle_type}) distilled, confidence={confidence:.2f}")


