EVOLUTION_LOG = "results/evolution_log.json"
REQUIRED_PACK_KEYS = {"version", "targets", "generation", "rules"}
REQUIRED_RULES_KEYS = {"branch_focus", "boundary_three_point", "loop_paths", "condition_matrix", "exception_policy", "quality"}
MAX_RULES_PER_CATEGORY = 15


def build_optimizer_prompt(
    pack: dict,
    feedback: dict,
    evolution_entries: list,
    principles: list,
    round_num: int,
    max_changes: int = 2,
) -> tuple[str, str]:
    system = OPTIMIZER_SYSTEM_PROMPT.format(max_changes=max_changes, round_num=round_num)

    # Evolution history text
    if evolution_entries:
        history_lines = []
        for e in evolution_entries:
            cov = e.get("coverage_before", {})
            b = cov.get("branch", "?")
            changes = e.get("changes_made", "?")
            history_lines.append(f"- iter {e.get('iter', '?')}: branch={b}%, changes={changes}")
        evolution_text = "\n".join(history_lines)
    else:
        evolution_text = "(no prior rounds)"

    # Principles text
    if principles:
        prin_lines = []
        for p in principles[:15]:
            ptype = p.get("type", "unknown")
            summary = p.get("change_summary", "?")
            delta = p.get("coverage_delta", {})
            conf = p.get("confidence", 0)
            project = p.get("project", "?")
            prin_lines.append(f"- [{ptype}] ({project}) {summary} (branch_delta={delta.get('branch', 0):+.1f}%, confidence={conf:.2f})")
        principles_text = "\n".join(prin_lines)
    else:
        principles_text = "(no principles yet)"

    # Trim feedback to essential fields
    feedback_slim = {
        "method_gaps": feedback.get("method_gaps", [])[:15],
        "failure_taxonomy": feedback.get("failure_taxonomy", {}),
    }

    user = OPTIMIZER_USER_TEMPLATE.format(
        skill_pack_json=json.dumps(pack, indent=2, ensure_ascii=False),
        feedback_json=json.dumps(feedback_slim, indent=2, ensure_ascii=False),
        history_count=len(evolution_entries),
        evolution_history=evolution_text,
        principles_text=principles_text,
    )
    return system, user


def call_llm_optimizer(system: str, user: str, model: str, cwd: Path) -> str | None:
    prompt = f"<system>\n{system}\n</system>\n\n{user}"
    args = ["opencode", "run", prompt, "-m", model]
    try:
        result = subprocess.run(
            args,
            cwd=str(cwd),
            check=False,
            text=True,
            capture_output=True,
            timeout=120,
        )
        if result.returncode != 0:
            print(f"[optimizer] LLM call failed: exit_code={result.returncode}")
            if result.stderr:
                for line in result.stderr.strip().splitlines()[:5]:
                    print(f"[optimizer]   {line}")
            return None
        return result.stdout
    except subprocess.TimeoutExpired:
        print("[optimizer] LLM call timed out (120s)")
        return None
    except Exception as exc:
        print(f"[optimizer] LLM call error: {exc}")
        return None


def extract_json_from_response(text: str) -> dict | None:
    # Try ```json fenced block first
    match = re.search(r"```json\s*\n(.*?)```", text, re.DOTALL)
    if match:
        try:
            return json.loads(match.group(1))
        except json.JSONDecodeError:
            pass

    # Try any ``` fenced block
    match = re.search(r"```\s*\n(.*?)```", text, re.DOTALL)
    if match:
        try:
            return json.loads(match.group(1))
        except json.JSONDecodeError:
            pass

    # Try bare JSON (find first { to last })
    start = text.find("{")
    end = text.rfind("}")
    if start != -1 and end > start:
        try:
            return json.loads(text[start : end + 1])
        except json.JSONDecodeError:
            pass

    return None


def validate_skill_pack(candidate: dict, original: dict) -> list[str]:
    errors = []

    # Check required top-level keys
    missing = REQUIRED_PACK_KEYS - set(candidate.keys())
    if missing:
        errors.append(f"missing top-level keys: {missing}")

    # Check targets not lowered
    orig_targets = original.get("targets", {})
    cand_targets = candidate.get("targets", {})
    for key in ("line_pct", "branch_pct", "method_pct"):
        orig_val = orig_targets.get(key, 0)
        cand_val = cand_targets.get(key, 0)
        if isinstance(cand_val, (int, float)) and cand_val < orig_val:
            errors.append(f"target {key} lowered: {orig_val} -> {cand_val}")

    # Check rules structure
    cand_rules = candidate.get("rules", {})
    if not isinstance(cand_rules, dict):
        errors.append("rules is not a dict")
    else:
        missing_rules = REQUIRED_RULES_KEYS - set(cand_rules.keys())
        if missing_rules:
            errors.append(f"missing rule categories: {missing_rules}")
        for cat, items in cand_rules.items():
            if not isinstance(items, list):
                errors.append(f"rules.{cat} is not a list")
            elif len(items) > MAX_RULES_PER_CATEGORY:
                errors.append(f"rules.{cat} has {len(items)} items (max {MAX_RULES_PER_CATEGORY})")

    # Check generation structure
    cand_gen = candidate.get("generation", {})
    if not isinstance(cand_gen, dict):
        errors.append("generation is not a dict")

    return errors


def diff_skill_packs(old: dict, new: dict) -> list[dict]:
    changes = []

    def _compare(prefix: str, o, n):
        if isinstance(o, dict) and isinstance(n, dict):
            all_keys = set(o.keys()) | set(n.keys())
            for k in sorted(all_keys):
                _compare(f"{prefix}.{k}" if prefix else k, o.get(k), n.get(k))
        elif isinstance(o, list) and isinstance(n, list):
            if o != n:
                changes.append({
                    "param": prefix,
                    "before": o,
                    "after": n,
                    "expected_benefit": "LLM-proposed change",
                })
        elif o != n:
            changes.append({
                "param": prefix,
                "before": o,
                "after": n,
                "expected_benefit": "LLM-proposed change",
            })

    _compare("", old, new)
    return changes


def load_all_principles(principles_dir: str, limit: int = 15) -> list[dict]:
    """Load and merge principles from all project files in the directory."""
    if not principles_dir:
        return []
    pdir = Path(principles_dir)
    if not pdir.is_dir():
        return []
    all_principles = []
    for f in sorted(pdir.glob("*.json")):
        try:
            data = json.loads(f.read_text(encoding="utf-8"))
            for p in data.get("principles", []):
                p.setdefault("project", f.stem)
                all_principles.append(p)
        except (json.JSONDecodeError, OSError):
            continue
    # Sort by timestamp descending, take most recent
    all_principles.sort(key=lambda x: x.get("timestamp", ""), reverse=True)
    return all_principles[:limit]


def build_proposal_llm(
    report: dict,
    feedback: dict,
    pack: dict,
    round_num: int,
    model: str,
    principles_dir: str,
    cwd: Path,
    max_step_changes: int = 2,
) -> tuple[dict, dict] | None:
    # Load evolution log
    evo_path = cwd / EVOLUTION_LOG
    evolution_entries = []
    if evo_path.exists():
        try:
            evo_log = json.loads(evo_path.read_text(encoding="utf-8"))
            evolution_entries = evo_log.get("entries", [])[-5:]
        except (json.JSONDecodeError, OSError):
            pass

    # Load principles from all projects
    principles = load_all_principles(principles_dir)

    system, user = build_optimizer_prompt(pack, feedback, evolution_entries, principles, round_num, max_step_changes)

    raw = call_llm_optimizer(system, user, model, cwd)
    if raw is None:
        return None

    candidate = extract_json_from_response(raw)
    if candidate is None:
        # Retry once with a fix prompt
        print("[optimizer] LLM output had no valid JSON, retrying with fix prompt...")
        fix_prompt = (
            "Your previous response did not contain valid JSON. "
            "Please output ONLY the complete skill_pack.json inside a ```json block. No other text."
        )
        raw2 = call_llm_optimizer(system, fix_prompt, model, cwd)
        if raw2 is not None:
            candidate = extract_json_from_response(raw2)
        if candidate is None:
            print("[optimizer] LLM retry also failed to produce valid JSON")
            return None

    errors = validate_skill_pack(candidate, pack)
    if errors:
        print(f"[optimizer] LLM proposal validation failed: {errors}")
        # Retry once
        fix_prompt = (
            f"Your proposed skill_pack.json has validation errors:\n"
            + "\n".join(f"- {e}" for e in errors)
            + "\n\nPlease fix these issues and output the corrected complete JSON in a ```json block."
        )
        raw2 = call_llm_optimizer(system, fix_prompt, model, cwd)
        if raw2 is not None:
            candidate = extract_json_from_response(raw2)
            if candidate is not None:
                errors = validate_skill_pack(candidate, pack)
        if errors:
            print(f"[optimizer] LLM retry still has validation errors: {errors}")
            return None

    parameter_changes = diff_skill_packs(pack, candidate)
    total = get_total(report)
    method_gaps = feedback.get("method_gaps", [])
    weak_methods = method_gaps[:8]

    # Extract reasoning from LLM response
    reasoning = ""
    reasoning_match = re.search(r"##\s*Reasoning\s*\n(.*)", raw, re.DOTALL)
    if reasoning_match:
        reasoning = reasoning_match.group(1).strip()[:500]

    proposal = {
        "round": round_num,
        "timestamp": datetime.now(timezone.utc).isoformat(),
        "before_total": total,
        "changes": [ch["param"] + ": changed" for ch in parameter_changes[:10]],
        "max_step_changes": max_step_changes,
        "parameter_changes": parameter_changes,
        "skipped_candidates": [],
        "focus_hints": [f"{x['class']}.{x['method']} branch={x['branch']}%" for x in weak_methods],
        "method": "llm",
        "reasoning": reasoning,
    }

    risk_level, risk_reasons = classify_risk(parameter_changes)
    proposal["risk_level"] = risk_level
    proposal["risk_reasons"] = risk_reasons
    proposal["expected_benefit"] = reasoning[:200] if reasoning else "LLM-proposed optimization"

    if risk_level != "low":
        proposal["changes"].append(f"risk_assessment={risk_level}: " + "; ".join(risk_reasons))

    # Ensure version is set
    candidate.setdefault("version", f"v3.{round_num}")
    candidate.setdefault("focus_hints", proposal["focus_hints"])

    return proposal, candidate


