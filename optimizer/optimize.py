def get_total(report: dict) -> dict:
    if isinstance(report.get("_total"), dict):
        return report["_total"]
    coverage = report.get("coverage", {})
    return {
        "line": coverage.get("line", {}).get("pct", 0.0),
        "branch": coverage.get("branch", {}).get("pct", 0.0),
        "method": coverage.get("method", {}).get("pct", 0.0),
    }


def classify_risk(parameter_changes: list) -> tuple[str, list[str]]:
    if not parameter_changes:
        return "low", ["no parameter change applied"]

    risk_score = 0
    reasons = []
    if len(parameter_changes) >= 2:
        risk_score = max(risk_score, 1)
        reasons.append("two parameters changed in one round")
    if any(ch.get("param", "").startswith("generation.") for ch in parameter_changes):
        risk_score = max(risk_score, 1)
        reasons.append("generation knobs may increase test volume and noise")
    if any(ch.get("param", "").startswith("targets.") for ch in parameter_changes):
        risk_score = max(risk_score, 2)
        reasons.append("target threshold changes affect optimization objective directly")

    level = "high" if risk_score >= 2 else "medium" if risk_score == 1 else "low"
    return level, reasons


def summarize_expected_benefit(parameter_changes: list) -> str:
    params = {ch.get("param", "") for ch in parameter_changes}
    focus = []
    if "generation.min_cases_per_method" in params or "rules.branch_focus" in params:
        focus.append("raise branch path hit probability")
    if "rules.boundary_three_point" in params:
        focus.append("improve line coverage on structural edge inputs")
    if "rules.quality" in params:
        focus.append("improve method-level invocation completeness")
    if not focus:
        return "No expected gain because no parameter was changed."
    return "Expected benefit: " + "; ".join(focus) + "."


