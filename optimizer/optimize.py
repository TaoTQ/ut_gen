def render_skill(script_root: Path, skill_pack: Path, skill_output: Path):
    renderer = script_root / "agent" / "skills" / "render_skill.py"
    cmd = [sys.executable, str(renderer), "--pack", str(skill_pack), "--output", str(skill_output)]
    subprocess.run(cmd, check=True)


def run_gate(gate_cmd: str):
    if not gate_cmd:
        return {"executed": False, "exit_code": None}
    result = subprocess.run(gate_cmd, shell=True)
    return {"executed": True, "exit_code": result.returncode}


def rollback_to_pack(root: Path, old_pack: dict, skill_pack_path: Path, skill_output_path: Path):
    skill_pack_path.write_text(json.dumps(old_pack, indent=2, ensure_ascii=False), encoding="utf-8")
    render_skill(root, skill_pack_path, skill_output_path)


def extract_quality(report: dict) -> dict:
    test_execution = report.get("test_execution", {})
    quality = report.get("quality", {})
    pass_rate = test_execution.get("pass_rate_pct")
    try:
        pass_rate = float(pass_rate) if pass_rate is not None else None
    except (TypeError, ValueError):
        pass_rate = None
    return {
        "pass_rate_pct": pass_rate,
        "compile_failed": bool(quality.get("compile_failed", False)),
    }


def evaluate_gate(before_report: dict, after_report: dict, branch_tolerance: float = 0.2, pass_rate_drop_tol: float = 3.0):
    before_total = get_total(before_report)
    after_total = get_total(after_report)
    before_quality = extract_quality(before_report)
    after_quality = extract_quality(after_report)
    reject_reasons = []

    before_branch = float(before_total.get("branch", 0.0))
    after_branch = float(after_total.get("branch", 0.0))
    if after_branch + branch_tolerance < before_branch:
        reject_reasons.append(
            f"branch_drop_exceeded: before={before_branch:.2f} after={after_branch:.2f} tolerance={branch_tolerance:.2f}"
        )

    if not before_quality["compile_failed"] and after_quality["compile_failed"]:
        reject_reasons.append("compile_failure_rate_increased: before=0 after=1")

    before_pass = before_quality["pass_rate_pct"]
    after_pass = after_quality["pass_rate_pct"]
    if before_pass is not None and after_pass is not None:
        if after_pass + pass_rate_drop_tol < before_pass:
            reject_reasons.append(
                f"pass_rate_drop_exceeded: before={before_pass:.2f} after={after_pass:.2f} tolerance={pass_rate_drop_tol:.2f}"
            )

    return {
        "accept": len(reject_reasons) == 0,
        "reject_reasons": reject_reasons,
        "before_total": before_total,
        "after_total": after_total,
        "before_quality": before_quality,
        "after_quality": after_quality,
    }


def reject_with_rollback(
    root: Path,
    old_pack: dict,
    skill_pack_path: Path,
    skill_output_path: Path,
    decision: str,
    reason: str,
    reject_reasons: list,
):
    reject_reasons.append(reason)
    rollback_to_pack(root, old_pack, skill_pack_path, skill_output_path)
    return decision


